package object_in;

public class Person {
	private int age;
	private String name;
	private double height;
	private double weight;
	public Person(int age, String name){
		this.age = age;
		this.name = name;
		weight = this.age;
		height = this.age;
	}
	public Person(double height, double weight){
		this.height = height;
		this.weight = weight;
	}
}
