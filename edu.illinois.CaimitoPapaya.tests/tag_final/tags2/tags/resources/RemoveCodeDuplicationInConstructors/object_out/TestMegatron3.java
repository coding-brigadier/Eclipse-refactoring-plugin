package object_in;

public class Person {
	private int age;
	private String name;
	private double height;
	private double weight;
	public Person(int inputAge, String inputName){
		this(inputAge,inputAge,0,0);
	}
	public Person(double height, double weight){
		this(0,0,height,weight);
	}
	public Person(int inputAge, String inputName, double height, double weight){
		this.age = inputAge;
		this.name = inputName;
		this.height = height;
		this.weight = weight;
	}
}
